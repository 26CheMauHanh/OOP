/*Bai 2: Viết một chương trình quản lý Chi tiêu
- Yêu cầu xây dụng lớp Chi tiêu: Income, Outcom, Balance, Cost. Viết ohuowng thức khởi tạo dùng tham chiếu this, từ khóa get, set,..
- Tính mức chi tiêu (Cost) và số dư (Balance) hiện có.
- Biết rằng mức chi tiêu được tính bằng: Cost=(Outcome/Income)*100%, số dư được tính bằng: Balance=Income - Outcome. Trường hợp Balance <= 10$ hoặc Mức chi tiêu >=90%, thì đưa ra cảnh báo chi tiêu "Sắp hết tiền"
*/
class CHITIEU
{
    double Income, Outcome;
    public CHITIEU(double Income, double Outcome)
    {
        this.Income = Income;
        this.Outcome = Outcome;
    }
    public double Balance
    {
        get{return Income.}
    }
}
